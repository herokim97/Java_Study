package ch13;


public class chap13 {

	public static void main(String[] args) {

		int a = 5;
		int b = 10;
		
		System.out.println(a | b);
		System.out.println(a & b);
		System.out.println(a ^ b);
		System.out.println(~a);
		
		System.out.println(a << 2);
		System.out.println(a);
		System.out.println(a <<= 2);
		System.out.println();
		

	}

}
